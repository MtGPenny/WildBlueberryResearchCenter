                            <div class="panel-heading">
                                <h4 class="panel-title">
                                    <a data-toggle="collapse" data-parent="#accordion2" href="#collapseThreeSeven">
                                        What is the difference between wild and cultivated blueberries, highbush
                                        and lowbush blueberries?
                                    </a>
                                </h4>
                            </div>
                            <div id="collapseThreeSeven" class="panel-collapse collapse">
                                <div class="panel-body">
                                    The term wild and lowbush basically refer to the same plant. Traditionally the term
                                    lowbush was used by extension, researchers and educators. Wild was used by the trade and
                                    marketing sectors. Now, the term wild is being accepted and used by all sectors of the North American
                                    industry. These plants naturally occur in Eastern Canada and Maine, and are managed on a
                                    commercial basis. Some lowbush blueberries are planted for production and therefore the word wild
                                    would not be used. Cultivated blueberries and highbush refer to the same plant. These are a larger growing shrub
                                    than lowbush and produce a larger fruit. Cultivated bushes are planted in rows. Most
                                    of this fruit is sold fresh compared to wild blueberries which are mostly processed and sold
                                    frozen. These two products compete together on the wild market.
                                </div>
                            </div>

