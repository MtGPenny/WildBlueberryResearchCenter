                                <div class="panel-heading">
                                    <h4 class="panel-title">
                                        <a data-toggle="collapse" data-parent="#accordion2" href="#collapseThreeSix">
                                            Why prune lowbush blueberries?
                                        </a>
                                    </h4>
                                </div>
                                <div id="collapseThreeSix" class="panel-collapse collapse">
                                    <div class="panel-body">
                                        Chris Prouse, Co-ordinator - Agriculture Resources Team
                                        Prince Edward Island Agriculture & Forestry
                                        The main reason lowbush blueberries are pruned is to increase yields and
                                        possibly stimulate runner growth. Blueberries do not produce flowers on the first years growth. On
                                        second year growth the maximum number of flowers are produced. On older growth (third, forth
                                        year etc.) there will still be flowers produced but in general the number of flowers will
                                        be less. In addition to maximizing yields,weed control is more effective if you prune the
                                        blueberries. In fact, some of the herbicides can only be used after the plants have pruned.
                                    </div>
